package gson;

public class Quizzes {
    String[] Id ;
    String[] Name ;

    public void setName(String[] name) {
        Name = name;
    }

    public void setId(String[] id) {
        Id = id;
    }

    public String[] getId() {
        return Id;
    }

    public String[] getName() {
        return Name;
    }
}
